<h1>Adoption Showcase</h1>
 <p>Rabbit Haven's ultimate goal is the permanent placement of our rabbits in safe, 
	       loving homes.&nbsp; Adoption counseling, visitation, and education services are 
	       available to the general public.&nbsp; We require that all rabbit caretakers 
	       provide permanent homes, a safe environment, proper food, grooming, medical 
	       attention, and the love and support essential to the rabbit's well-being.&nbsp; 
	       We recommend that all rabbits be kept indoors and that they be allowed to live
               <u><strong>with</strong></u> the family and not apart (i.e., set aside in a back 
	       yard hutch).
      </p>

									
            <p>Rabbit Haven only adopts to families who agree to keep rabbits indoors. Outside 
	       arrangements are discouraged due to early lack of social setting, early deaths 
	       caused by exposure to disease agents, such as the very deadly <a href="http://www.medirabbit.com/EN/Skin_diseases/Viral_diseases/myxomatosis.PDF">
	       <strong>Myxomatosis</strong></a>, and predators that may harm the rabbit.&nbsp; All new adoptive families receive discount coupons from area pet 
	       supply retailers, and an informative information packet.
            </p>

									
